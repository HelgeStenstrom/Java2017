package Helge

/**
 * Systembolaget divides wines into character types. These are the types.
 */
enum class CharacterType {

    // Red
    Fruktigt_och_smakrikt,
    Mjukt_och_bärigt,
    Kryddigt_och_mustigt,
    Stramt_och_nyanserat,

    // White
    Friskt_och_fruktigt,
    Fylligt_och_smakrikt,
    Druvigt_och_blommigt,
    Lätt_och_avrundat
}
